import { ExecutorContext, logger, ProjectConfiguration } from '@nx/devkit';
import * as esbuild from 'esbuild';
import { glob } from 'glob';
import path from 'path';
import { UnknownRecord } from 'type-fest';
import * as zod from 'zod';
import { BuildExecutorSchema } from '../schema';
import vm from 'node:vm';

/** Schema wrapper context shape */
export interface SchemaWrapperContext {
  /** User schema module */
  module?: UnknownRecord;
  /** Bundled zod module reference */
  z?: typeof zod;
}

/** Schema source file glob pattern */
const SCHEMA_SOURCE_FILES_GLOB = '**/*.schema.ts';

/**
 * Finds all schema source files in a project
 *
 * @param context Executor context
 * @param config Project configuration
 * @returns An array of absolute paths to schema source files
 */
export async function findSchemaModules(context: ExecutorContext, config: ProjectConfiguration): Promise<string[]> {
  const projectDir = path.join(context.root, config.root);
  return await glob(SCHEMA_SOURCE_FILES_GLOB, { absolute: true, cwd: projectDir });
}

/**
 * Compile a schema source file with esbuild
 *
 * @param file Source file
 * @param verbose Whether to enable verbose logging
 * @returns File content
 */
export async function compileSchemaModule(file: string, verbose = false): Promise<esbuild.OutputFile> {
  const fileName = path.basename(file, '.ts');
  const result = await esbuild.build({
    bundle: true,
    external: ['@angular/*', 'ng-inline-svg-2'],
    format: 'cjs',
    logLevel: verbose ? 'warning' : 'silent',
    stdin: {
      contents: `
          import * as z from 'zod';
          import * as module from './${fileName}';

          globalThis.module = module;
          globalThis.z = z;
        `,
      loader: 'ts',
      resolveDir: path.resolve(path.dirname(file)),
      sourcefile: `${fileName}.generated.ts`,
    },
    write: false,
  });

  return result.outputFiles[0];
}

/**
 * Loads a schema wrapper module file generated by `compileSchemaModule`
 *
 * @param content Compiled schema file script
 * @returns Loaded wrapper module
 */
export async function loadSchemaModule(content: esbuild.OutputFile): Promise<SchemaWrapperContext> {
  try {
    const context: SchemaWrapperContext = {};
    vm.runInNewContext(content.text, context, {
      filename: path.basename(content.path),
      importModuleDynamically: vm.constants.USE_MAIN_CONTEXT_DEFAULT_LOADER,
      timeout: 2000,
    });

    return context;
  } catch (error) {
    logger.error(`Failed to load '${path.basename(content.path)}': ${error}`);
    return {};
  }
}

/**
 * Test whether a module has a schema default export
 *
 * @param module Module to test
 * @param schemaBaseClass Base class for all zod schemas
 * @returns true if the module has a default export that is a zod schema, false otherwise
 */
function hasSchemaExport(
  module: UnknownRecord,
  schemaBaseClass: typeof zod.ZodType,
): module is { default: zod.ZodType } {
  return 'default' in module && module['default'] instanceof schemaBaseClass;
}

/**
 * Convert a zod schema from the wrapper module into a json schema
 *
 * @param file Source file path
 * @param wrapper Schema wrapper module
 * @param options Build options
 * @returns A json stringified schema
 */
export function convertSchemaModule(
  file: string,
  wrapper: SchemaWrapperContext,
  options: BuildExecutorSchema,
): string | undefined {
  const { module, z } = wrapper;
  if (!module || !z) {
    return undefined;
  }

  if (!hasSchemaExport(module, z.ZodType)) {
    logger.warn(`File "${path.basename(file)}" doesn't have a schema default export. Skipping...`);
    return undefined;
  }

  const jsonSchema = z.toJSONSchema(module['default'], {
    target: options.target,
    cycles: options.cycles,
    reused: options.reused,
    unrepresentable: options.unrepresentable,
  });

  return JSON.stringify(jsonSchema, undefined, 2);
}
